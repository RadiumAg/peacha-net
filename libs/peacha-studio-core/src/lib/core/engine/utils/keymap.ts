export function getShortcutMap(shortcut: string): number[] {
    const keys = shortcut.split('+');
    return keys.map(k => {
        if (strToKeyCode[k] != undefined) {
            return strToKeyCode[k];
        }
        throw new Error(`${k} is not a valid key name`);
    }).sort();
}

